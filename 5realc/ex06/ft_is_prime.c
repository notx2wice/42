int		ft_is_prime(int nb)
{
	int x;

	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	x = 2;
	while ((x * x) <= nb && x < 66000)
	{
		if (nb % x == 0)
		{
			return (0);
		}
		x++;
	}
	return (1);
}
