int		ft_fibonacci(int idx)
{
	if (idx < 0)
		return (-1);
	if (idx == 0)
		return (0);
	if (idx == 1)
		return (1);
	return (ft_fibonacci(idx - 1) + ft_fibonacci(idx - 2));
}
