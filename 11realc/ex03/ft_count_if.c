int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	x;
	int	ans;

	ans = 0;
	x = 0;
	while (x < length)
	{
		if (f(tab[x]) != 0)
			ans++;
		x++;
	}
	return (ans);
}
