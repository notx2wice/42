#include <unistd.h>

void	ft_is_negative(int x)
{
	char n;
	char p;

	n = 'N';
	p = 'P';
	if (x >= 0)
	{
		write(1, &p, 1);
	}
	else
	{
		write(1, &n, 1);
	}
}
